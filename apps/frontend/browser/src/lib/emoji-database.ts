export const getEmojisByCategory = (category: EmojiCategory): EmojiData[] => {
  return EMOJI_DATABASE.filter((emoji) => emoji.category === category);
};

export const searchEmojis = (query: string): EmojiData[] => {
  const searchLower = query.toLowerCase();
  return EMOJI_DATABASE.filter((emojiData) => {
    const searchableText = [emojiData.name, ...emojiData.keywords]
      .join(' ')
      .toLowerCase();
    return searchableText.includes(searchLower);
  });
};

export const getAllCategories = (): string[] => {
  return [...new Set(EMOJI_DATABASE.map((emoji) => emoji.category))];
};
